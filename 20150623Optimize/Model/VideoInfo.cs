using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace VAGastronomistMobileApp.Model
{
    /// <summary>
    /// 菜的视频信息
    /// </summary>
    public class VideoInfo
    {
        /// <summary>
        /// 视频编号
        /// </summary>
        public int VideoID { get; set; }
        /// <summary>
        /// 对应菜编号
        /// </summary>
        public int DishID { get; set; }
        /// <summary>
        /// 视频名称
        /// </summary>
        public string VideoName { get; set; }
        /// <summary>
        /// 视频序号
        /// 同一菜多视频排序
        /// </summary>
        public int VideoSequence { get; set; }
        /// <summary>
        /// 视频规格
        /// 备用
        /// </summary>
        public int VideoScale { get; set; }
        /// <summary>
        /// 视频状态（是否删除）
        /// 0：已删除，1：正常
        /// </summary>
        public int VideoStatus { get; set; }
    }
}
