using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace VAGastronomistMobileApp.Model
{
    /// <summary>
    /// 公司预支付奖励信息
    /// </summary>
    public class PrePayPrivilege
    {
        /// <summary>
        /// 编号
        /// </summary>
        public int id { get; set; }
        /// <summary>
        /// 店铺编号
        /// </summary>
        public int shopId { get; set; }
        /// <summary>
        /// 预支付奖励描述
        /// </summary>
        public string prePayPrivilegeStr { get; set; }
        /// <summary>
        /// 预支付返现金额类型
        /// </summary>
        public VAPrePayCashBackType prePayCashBackType { get; set; }
        /// <summary>
        /// 预支付返现金额值
        /// </summary>
        public double prePayCashBackValue { get; set; }
        /// <summary>
        /// 是否有效
        /// </summary>
        public int isValid { get; set; }
        /// <summary>
        /// 预支付需要提前的时间（小时）
        /// </summary>
        public double numberOfHoursAhead { get; set; }
        /// <summary>
        /// 预支付是否支持返现
        /// </summary>
        public int supportPrePayCashBack { get; set; }
        /// <summary>
        /// 预支付是否支持免排队
        /// </summary>
        public int supportPrePayVIPEntrance { get; set; }
        /// <summary>
        /// 预支付是否支持送礼品
        /// </summary>
        public int supportPrePayGift { get; set; }
        /// <summary>
        /// 预支付免排队每日限额
        /// </summary>
        public int vipEntranceMax { get; set; }
        /// <summary>
        /// 预支付礼品内容
        /// </summary>
        public string giftContent { get; set; }
        /// <summary>
        /// 预支付返现最低预付金额（返券最低金额）
        /// </summary>
        public double prePayCashMin { get; set; }
        /// <summary>
        /// 相对时间和绝对时间
        /// </summary>
        public int timeType { get; set; }
        /// <summary>
        /// 相对时间天数
        /// </summary>
        public double dayCount { get; set; }
        /// <summary>
        /// 生效时间
        /// </summary>
        public double effectTime { get; set; }
        /// <summary>
        /// 时间段返此券张数
        /// </summary>
        public int useCountinTime { get; set; }
        /// <summary>
        /// 支付返券时间间隔（eg：5个小时内，此券只能返2张）
        /// </summary>
        public double returnCouponInterval { get; set; }
    }
}
