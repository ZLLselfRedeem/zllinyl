using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace VAGastronomistMobileApp.Model
{
    /// <summary>
    /// 功能描述:菜的配料Id
    /// 创建标识:罗国华 20131030
    /// </summary>
    public class DishIngredients
    {
        /// <summary>
        /// 菜的配料Id
        /// </summary>
        public int ingredientsId { get; set; }
        /// <summary>
        /// 菜谱Id
        /// </summary>
        public int menuId { get; set; }
        /// <summary>
        /// 配料名称
        /// </summary>
        public string ingredientsName { get; set; }
        /// <summary>
        /// 配料价格
        /// </summary>
        public double ingredientsPrice { get; set; }
        /// <summary>
        /// 是否能享受Vip折扣
        /// </summary>
        public bool vipDiscountable { get; set; }
        /// <summary>
        /// 是否支持返送
        /// </summary>
        public bool backDiscountable { get; set; }
        /// <summary>
        /// 备注
        /// </summary>
        public string ingredientsRemark { get; set; }
        /// <summary>
        /// 排序号
        /// </summary>
        public int ingredientsSequence { get; set; }
        /// <summary>
        /// 状态
        /// </summary>
        public bool ingredientsStatus { get; set; }

    }
}