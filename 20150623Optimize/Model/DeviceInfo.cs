using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace VAGastronomistMobileApp.Model
{
    /// <summary>
    /// 设备信息
    /// </summary>
    public class DeviceInfo
    {
        /// <summary>
        /// 设备编号
        /// </summary>
        public long deviceId { get; set; }
        /// <summary>
        /// 客户端编号
        /// </summary>
        public string uuid { get; set; }
        /// <summary>
        /// 客户端推送码
        /// </summary>
        public string pushToken { get; set; }
