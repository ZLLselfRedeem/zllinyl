using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace VAGastronomistMobileApp.Model
{
    /// <summary>
    /// 客户端出错日志
    /// </summary>
    public class ClientErrorInfo
    {
        /// <summary>
        /// 编号
        /// </summary>
        public long id { get; set; }
        /// <summary>
        /// 日志创建时间
        /// </summary>
        public DateTime time { get; set; }
        /// <summary>
        /// 日志内容
        /// </summary>
        public string errorMessage { get; set; }
        /// <summary>
        /// 客户端类型
        /// </summary>
        public VAAppType appType { get; set; }
        /// <summary>
        /// 客户端版本号
        /// </summary>
        public string clientBuild { get; set; }
        /// <summary>
        /// 客户端类型
        /// </summary>
        public VAClientType clientType { get; set; }
    }
}
