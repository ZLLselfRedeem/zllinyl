CREATE TABLE [dbo].[StocktakingInfo] (
    [ID]                  INT           IDENTITY (1, 1) NOT NULL,
    [wareHouseID]         INT           NULL,
    [materialID]          INT           NULL,
    [stockTakingTime]     DATETIME      NULL,
    [quantity]            FLOAT (53)    NULL,
    [stockTakingQuantity] FLOAT (53)    NULL,
    [stockTakingManager]  VARCHAR (50)  NULL,
    [remark]              VARCHAR (100) NULL,
    CONSTRAINT [PK_StocktakingInfo] PRIMARY KEY CLUSTERED ([ID] ASC)
);

