CREATE TABLE [dbo].[MaterialInfo] (
    [materialID]       INT          IDENTITY (1, 1) NOT NULL,
    [materialName]     VARCHAR (50) NULL,
    [materialQuanPin]  VARCHAR (50) NULL,
    [materialJianPin]  VARCHAR (50) NULL,
    [purchaseUnit]     INT          NULL,
    [stockKeepingUnit] INT          NULL,
